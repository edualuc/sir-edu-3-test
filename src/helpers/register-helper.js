
export const handleChange = (event, id, valueParam) => {
    let name = '';
    let value = null;

    if (event) {
        name = event.target.name;
        value = event.target.value;
    } else {
        name = id;
        value = valueParam;
    }
    this.addStudentTemporario(name, value)
    this.forceUpdate()
}
